/**
 * BOK INTELLIGENCE LAYER
 * Advanced analytics for Book of Knowledge
 * Tracks: Market condition, Time of day, Token category performance
 */

const fs = require('fs');
const path = require('path');

const CONFIG = {
  BOK_DIR: '/root/trading-bot/bok',
  INTELLIGENCE_FILE: '/root/trading-bot/bok/20-intelligence-analytics.md',
  LIVE_TRACKER: '/root/trading-bot/live-strategy-tracker.json',
  MARKET_CONDITION: '/root/trading-bot/market-condition.json',
  TRADES_LOG: '/root/trading-bot/live-trades.log',
  POSITIONS_FILE: '/root/trading-bot/positions.json'
};

class BOKIntelligenceLayer {
  constructor() {
    this.intelligence = this.loadIntelligence();
  }

  loadIntelligence() {
    try {
      if (fs.existsSync(CONFIG.INTELLIGENCE_FILE)) {
        // Parse markdown to extract JSON
        const content = fs.readFileSync(CONFIG.INTELLIGENCE_FILE, 'utf8');
        const jsonMatch = content.match(/```json\n([\s\S]*?)\n```/);
        if (jsonMatch) {
          return JSON.parse(jsonMatch[1]);
        }
      }
    } catch (e) {}
    
    return {
      timePerformance: {},
      marketConditionPerformance: {},
      tokenCategoryPerformance: {},
      strategyMarketFit: {},
      optimalSettings: {},
      lastUpdate: null
    };
  }

  saveIntelligence() {
    this.intelligence.lastUpdate = new Date().toISOString();
    
    const markdown = `# BOK Intelligence Analytics

*Auto-generated advanced analytics for trading optimization*
*Updated: ${this.intelligence.lastUpdate}*

## Executive Summary

This report provides deep insights into strategy performance across different dimensions:
- Time of day analysis
- Market condition correlation
- Token category effectiveness
- Strategy-market fit scores

## JSON Data

\`\`\`json
${JSON.stringify(this.intelligence, null, 2)}
\`\`\`

## Time-Based Performance

${this.formatTimePerformance()}

## Market Condition Performance

${this.formatMarketPerformance()}

## Token Category Analysis

${this.formatCategoryPerformance()}

## Strategy-Market Fit

${this.formatStrategyFit()}

## Recommendations

${this.generateRecommendations()}

---
*Generated by BOK Intelligence Layer v1.0*
`;

    fs.writeFileSync(CONFIG.INTELLIGENCE_FILE, markdown);
  }

  loadPositions() {
    try {
      if (fs.existsSync(CONFIG.POSITIONS_FILE)) {
        return JSON.parse(fs.readFileSync(CONFIG.POSITIONS_FILE, 'utf8'));
      }
    } catch (e) {}
    return [];
  }

  loadMarketCondition() {
    try {
      if (fs.existsSync(CONFIG.MARKET_CONDITION)) {
        return JSON.parse(fs.readFileSync(CONFIG.MARKET_CONDITION, 'utf8'));
      }
    } catch (e) {}
    return { regime: 'UNKNOWN' };
  }

  categorizeToken(symbol) {
    const categories = {
      'MEME': ['bonk', 'wif', 'sam', 'shib', 'doge', 'pepe', 'floki', 'pump'],
      'DEFI': ['ray', 'orca', 'jup', 'jto', 'mngo', 'slnd'],
      'STABLE': ['usdc', 'usdt', 'usd1', 'pyusd'],
      'LST': ['msol', 'bsol', 'jsol', 'laine'],
      'GAMING': ['atlas', 'polis', 'gst', 'fitfi'],
      'AI': ['tao', 'render', 'rndr', 'ai', 'gpt'],
      'INFRA': ['sol', 'pyth', 'jito']
    };
    
    const lower = symbol.toLowerCase();
    for (const [category, keywords] of Object.entries(categories)) {
      if (keywords.some(k => lower.includes(k))) {
        return category;
      }
    }
    return 'OTHER';
  }

  analyzeTimePerformance() {
    const positions = this.loadPositions().filter(p => p.exited);
    const hourly = {};
    
    for (let i = 0; i < 24; i++) {
      hourly[i] = { wins: 0, losses: 0, total: 0, profit: 0 };
    }
    
    for (const pos of positions) {
      const hour = new Date(pos.entryTime || pos.timestamp).getHours();
      const pnl = (pos.exitPrice / pos.entryPrice - 1) * 100;
      
      hourly[hour].total++;
      hourly[hour].profit += pnl;
      
      if (pnl > 0) hourly[hour].wins++;
      else hourly[hour].losses++;
    }
    
    // Calculate WR for each hour
    for (const hour in hourly) {
      const h = hourly[hour];
      if (h.total > 0) {
        h.winRate = (h.wins / h.total * 100).toFixed(1);
        h.avgProfit = (h.profit / h.total).toFixed(2);
      }
    }
    
    this.intelligence.timePerformance = hourly;
    return hourly;
  }

  analyzeMarketConditionPerformance() {
    const positions = this.loadPositions().filter(p => p.exited && p.marketCondition);
    const byCondition = {};
    
    for (const pos of positions) {
      const condition = pos.marketCondition || 'UNKNOWN';
      
      if (!byCondition[condition]) {
        byCondition[condition] = { wins: 0, losses: 0, total: 0, profit: 0 };
      }
      
      const pnl = (pos.exitPrice / pos.entryPrice - 1) * 100;
      byCondition[condition].total++;
      byCondition[condition].profit += pnl;
      
      if (pnl > 0) byCondition[condition].wins++;
      else byCondition[condition].losses++;
    }
    
    // Calculate stats
    for (const condition in byCondition) {
      const c = byCondition[condition];
      if (c.total > 0) {
        c.winRate = (c.wins / c.total * 100).toFixed(1);
        c.avgProfit = (c.profit / c.total).toFixed(2);
      }
    }
    
    this.intelligence.marketConditionPerformance = byCondition;
    return byCondition;
  }

  analyzeTokenCategoryPerformance() {
    const positions = this.loadPositions().filter(p => p.exited);
    const byCategory = {};
    
    for (const pos of positions) {
      const category = this.categorizeToken(pos.symbol);
      
      if (!byCategory[category]) {
        byCategory[category] = { wins: 0, losses: 0, total: 0, profit: 0, tokens: new Set() };
      }
      
      const pnl = (pos.exitPrice / pos.entryPrice - 1) * 100;
      byCategory[category].total++;
      byCategory[category].profit += pnl;
      byCategory[category].tokens.add(pos.symbol);
      
      if (pnl > 0) byCategory[category].wins++;
      else byCategory[category].losses++;
    }
    
    // Calculate stats and convert sets to arrays
    for (const category in byCategory) {
      const c = byCategory[category];
      if (c.total > 0) {
        c.winRate = (c.wins / c.total * 100).toFixed(1);
        c.avgProfit = (c.profit / c.total).toFixed(2);
      }
      c.tokens = Array.from(c.tokens);
    }
    
    this.intelligence.tokenCategoryPerformance = byCategory;
    return byCategory;
  }

  calculateStrategyMarketFit() {
    const positions = this.loadPositions().filter(p => p.exited && p.strategyId && p.marketCondition);
    const fit = {};
    
    for (const pos of positions) {
      const key = `${pos.strategyId}_${pos.marketCondition}`;
      
      if (!fit[key]) {
        fit[key] = { 
          strategy: pos.strategyId,
          market: pos.marketCondition,
          wins: 0, 
          losses: 0, 
          total: 0, 
          profit: 0 
        };
      }
      
      const pnl = (pos.exitPrice / pos.entryPrice - 1) * 100;
      fit[key].total++;
      fit[key].profit += pnl;
      
      if (pnl > 0) fit[key].wins++;
      else fit[key].losses++;
    }
    
    // Calculate fit scores
    for (const key in fit) {
      const f = fit[key];
      if (f.total >= 3) { // Need at least 3 samples
        f.winRate = (f.wins / f.total * 100).toFixed(1);
        f.avgProfit = (f.profit / f.total).toFixed(2);
        f.fitScore = (parseFloat(f.winRate) * 0.6) + (parseFloat(f.avgProfit) * 4);
      }
    }
    
    this.intelligence.strategyMarketFit = fit;
    return fit;
  }

  findOptimalSettings() {
    const timePerf = this.intelligence.timePerformance;
    const marketPerf = this.intelligence.marketConditionPerformance;
    
    // Find best trading hours
    const bestHours = Object.entries(timePerf)
      .filter(([h, d]) => d.total >= 3)
      .sort((a, b) => parseFloat(b[1].winRate) - parseFloat(a[1].winRate))
      .slice(0, 3)
      .map(([h, d]) => ({ hour: parseInt(h), winRate: d.winRate, profit: d.avgProfit }));
    
    // Find best market conditions
    const bestConditions = Object.entries(marketPerf)
      .filter(([c, d]) => d.total >= 3)
      .sort((a, b) => parseFloat(b[1].winRate) - parseFloat(a[1].winRate))
      .slice(0, 3)
      .map(([c, d]) => ({ condition: c, winRate: d.winRate, profit: d.avgProfit }));
    
    this.intelligence.optimalSettings = {
      bestTradingHours: bestHours,
      bestMarketConditions: bestConditions,
      avoidHours: Object.entries(timePerf)
        .filter(([h, d]) => d.total >= 3 && parseFloat(d.winRate) < 40)
        .map(([h, d]) => parseInt(h)),
      avoidConditions: Object.entries(marketPerf)
        .filter(([c, d]) => d.total >= 3 && parseFloat(d.winRate) < 40)
        .map(([c, d]) => c)
    };
    
    return this.intelligence.optimalSettings;
  }

  formatTimePerformance() {
    const perf = this.intelligence.timePerformance;
    let md = '| Hour | Trades | Win Rate | Avg Profit | Status |\n';
    md += '|------|--------|----------|------------|--------|\n';
    
    for (let i = 0; i < 24; i++) {
      const p = perf[i];
      if (p.total > 0) {
        const status = parseFloat(p.winRate) >= 60 ? '🟢' : parseFloat(p.winRate) >= 40 ? '🟡' : '🔴';
        md += `| ${i}:00 | ${p.total} | ${p.winRate}% | ${p.avgProfit}% | ${status} |\n`;
      }
    }
    return md;
  }

  formatMarketPerformance() {
    const perf = this.intelligence.marketConditionPerformance;
    let md = '| Condition | Trades | Win Rate | Avg Profit |\n';
    md += '|-----------|--------|----------|------------|\n';
    
    for (const [condition, p] of Object.entries(perf)) {
      md += `| ${condition} | ${p.total} | ${p.winRate}% | ${p.avgProfit}% |\n`;
    }
    return md;
  }

  formatCategoryPerformance() {
    const perf = this.intelligence.tokenCategoryPerformance;
    let md = '| Category | Trades | Win Rate | Avg Profit | Tokens |\n';
    md += '|----------|--------|----------|------------|--------|\n';
    
    for (const [cat, p] of Object.entries(perf)) {
      const tokens = p.tokens?.slice(0, 3).join(', ') + (p.tokens?.length > 3 ? '...' : '');
      md += `| ${cat} | ${p.total} | ${p.winRate}% | ${p.avgProfit}% | ${tokens} |\n`;
    }
    return md;
  }

  formatStrategyFit() {
    const fit = this.intelligence.strategyMarketFit;
    const sorted = Object.entries(fit)
      .filter(([k, f]) => f.total >= 3)
      .sort((a, b) => parseFloat(b[1].fitScore) - parseFloat(a[1].fitScore))
      .slice(0, 10);
    
    let md = '| Strategy | Market | Trades | Win Rate | Fit Score |\n';
    md += '|----------|--------|--------|----------|-----------|\n';
    
    for (const [key, f] of sorted) {
      md += `| ${f.strategy} | ${f.market} | ${f.total} | ${f.winRate}% | ${f.fitScore?.toFixed(1) || 'N/A'} |\n`;
    }
    return md;
  }

  generateRecommendations() {
    const opt = this.intelligence.optimalSettings;
    let recs = '';
    
    if (opt.bestTradingHours?.length > 0) {
      recs += `### Best Trading Hours\n`;
      recs += `Trade during: ${opt.bestTradingHours.map(h => `${h.hour}:00`).join(', ')}\n\n`;
    }
    
    if (opt.bestMarketConditions?.length > 0) {
      recs += `### Best Market Conditions\n`;
      opt.bestMarketConditions.forEach(c => {
        recs += `- **${c.condition}**: ${c.winRate}% WR\n`;
      });
      recs += '\n';
    }
    
    if (opt.avoidHours?.length > 0) {
      recs += `### Avoid Hours\n`;
      recs += `Avoid trading: ${opt.avoidHours.map(h => `${h}:00`).join(', ')}\n\n`;
    }
    
    return recs || 'Insufficient data for recommendations yet.';
  }

  run() {
    console.log('='.repeat(60));
    console.log('🧠 BOK INTELLIGENCE LAYER');
    console.log('='.repeat(60) + '\n');
    
    console.log('📊 Analyzing time-based performance...');
    this.analyzeTimePerformance();
    
    console.log('📊 Analyzing market condition performance...');
    this.analyzeMarketConditionPerformance();
    
    console.log('📊 Analyzing token category performance...');
    this.analyzeTokenCategoryPerformance();
    
    console.log('📊 Calculating strategy-market fit...');
    this.calculateStrategyMarketFit();
    
    console.log('🔍 Finding optimal settings...');
    this.findOptimalSettings();
    
    console.log('\n💾 Saving intelligence report...');
    this.saveIntelligence();
    
    console.log('\n✅ Intelligence layer update complete');
    console.log(`📄 Report saved to: ${CONFIG.INTELLIGENCE_FILE}`);
  }
}

module.exports = BOKIntelligenceLayer;

// Run if called directly
if (require.main === module) {
  const intelligence = new BOKIntelligenceLayer();
  intelligence.run();
}
